const activeWars = new Map();

module.exports = {
    config: {
        name: "war2",
        aliases: ["nonstop", "target"],
        version: "3.0",
        author: "Ullash ッ ☆𝐀𝐁𝐇𝐑𝐀𝐍𝐈𝐋☆",
        role: 2, 
        category: "fun",
        shortDescription: "কমান্ড দিলে অটোমেটিক গালি শুরু হবে",
        longDescription: "যাকে মুরগি বানাতে চান তার মেসেজে রিপ্লাই দিয়ে কমান্ডটি লিখুন। বট একের পর এক গালি দিতে থাকবে যতক্ষণ না আপনি আবার কমান্ড দিয়ে বন্ধ করছেন।",
        guide: {
            en: "{p}war2 (Reply to a message)"
        }
    },

    onStart: async function ({ api, event }) {
        if (event.type !== "message_reply") return;

        const targetID = event.messageReply.senderID;
        const threadID = event.threadID;

        // যদি অলরেডি এই ইউজারকে গালি দেওয়া শুরু হয়ে থাকে, তবে বন্ধ করে দিবে
        if (activeWars.has(targetID)) {
            clearInterval(activeWars.get(targetID));
            activeWars.delete(targetID);
            return; 
        }

        const userInfo = await api.getUserInfo(targetID);
        const name = userInfo[targetID].name;

        const replies = [
            `@${name} ইব্রাহিম কে যে গালি দিবি তার আম্মু রে চুদি মাগির পোলা !`, // শুরু হবে এটা দিয়ে
            `@${name} ঠাপের নাম যদি হয় চুদা-- তোর মার ভোদায় দিমু এক বস্তা জুতা--- জুতায় আছে ময়লা-- তোর মার ভোদা চুইদা বাহির করমু এক বস্তা কয়লা💦💨😊`,
            `@${name} তোর মার ভোদা চুইদা ➖ তোর বোনের ভোদায় ভরমু এক বালতি বিচিরা ➖ বিচিরা রং হয় সাদা ➖তোর মার ভোদা চুইদা বানামু ভারচুয়াল জগতের গাধা❎`,
            `@${name} খাম্বা হয় শক্ত, তোর মার বোদা চুইদা বার করমু রক্ত--রক্তের রং যদি হয় লাল, তোর মারে চুদবো চিরকাল ❎`,
            `@${name} তোর মার কালা ভোদা চুদমু আমি ঘরের এক কনে 👒শুয়ারের বাচ্চা ❎`,
            `@${name} তোর মার ভোদা চুইদা করমু ১০০ইনচি ফাঁকা---💦💦 😃`,
            `@${name} ডিপ্রেশন থেকেও তোর মাইরে চু*** দি 🤬 বান্ধির পোলা`,
            `@${name} খানকির পোলা, আর একটা টেক্সট করলে তুই তোর মায়ের মাসিক খাস \n ধন্যবাদ 🫰🏼`,
            `@${name} ব্যাস্যার পোলা, কথা শুন, তোর আম্মু রে চুদি গামছা পেচিয়ে🐰`,
            `@${name} হিজলা মাগির পোলা, হাতির ল্যাওড়া দিয়া তোর মায়েরে চুদুম`,
            `@${name} টুকাই মাগির পোলা, তোর মার ভোদায় পাব্লিক টয়লেট।🤟`,
            `@${name} তোর টেক্সট কামলার মায়রে চু*দি 🤡`,
            `@${name} খানকির পোলা, টেক্সট না করে আমার ধন চুষে দে😂, টাকা দিমু`,
            `@${name} তোর মুখে হাইগ্যা দিমু। ভুস্কি মাগির পোলা 🤣`,
            `@${name} -𝐃𝐨𝐧'𝐭 𝐌𝐢𝐧𝐝, তোরেও চুদি সাথে তোর মারে ও চুদি 😪💋🫂`,
            `@${name} ____________𝐃𝐨𝐧'𝐭 𝐌𝐢𝐧𝐝 তোমার মারে চুদি!😴🤏🍆🐱🍒`,
            `@${name} ধোন চিন চিন করছে, হায় তোর আম্মুকে আমি চোদতে চাই! আমরা দুজন দুজনারি চোদার দুনিয়ার, তোর আম্মুরে ছুঁয়ে দিলে তোর কচি বোন প্রেগনেন্ট হয়ে যায় 🤣💃🤸‍♂️🫶`,
            `@${name} তোর আম্মুর বোদায় তালগাছ দিমু, খানকির পোলা!`,
            `@${name} আব্বু ডাক, মাপ করে দিমু!`,
            `@${name} তোর মায়রে বোদায় চেড়া সেন্ডেল দিয়া পিটামু, খানকির পোলা!`,
            `@${name} হাত মারা মাগির পোলা, ঘু খাইয়া মইরা যা 🫶🏼 🫵🏼 🤣`,
            `@${name} তোর মায়রে তোর বাপের কান্দে উপর পালাই চুদি 🤣💔🫤`,
            `@${name} তোর কচি বোনেরের তোর কোলে তুইলা চুদি 🙂 🐸 🤝`,
            `@${name} চিল্লাচিল্লি না কইরা তোর মায়ের ভোঁদার ছবি দে 🫰🏼, হাত মাইরা ঘুয়াইয়া পড়মু`,
            `@${name} তোর মায়ের ভোদা কালা😂😂`,
            `@${name} তোর মায়রে ছেড়া পলিথিন ব্যাগ দিয়া চু*দি 🤡🫶🏼😂😂`,
            `@${name} আমি তোর মায়রে চুদলাম, শুধুমাত্র মুহূর্তটাকে সুন্দর করার জন্য!🙂💔`,
            `@${name} এক আকাশ পরিমাণ কষ্ট নিয়ে তোর মারে চুদি 🙂💔💯`,
            `@${name} ___ পুটকি মারার জন্য তোর আম্মুকে চাই😚💚🍢`,
            `@${name} -আমার নুনু ছাড়া, তোর আম্মুর ভুদা 🥵 এতিম ..!!!🤫🫦💦`
        ];

        let index = 0;

        // প্রতি ২ সেকেন্ড (২০০০ মিলি সেকেন্ড) পর পর একটি করে গালি দিবে
        const interval = setInterval(() => {
            if (index >= replies.length) index = 1; // লুপ শেষ হলে আবার ১ থেকে শুরু হবে (০ নম্বরটা শুধু শুরুতে একবার)
            
            api.sendMessage({
                body: replies[index],
                mentions: [{ tag: `@${name}`, id: targetID }]
            }, threadID);
            
            index++;
        }, 2000); 

        activeWars.set(targetID, interval);
    }
};
